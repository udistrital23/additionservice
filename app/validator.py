from typing import Tuple


def suma_bases(numero_a: int, numero_b: int) -> int:
    """Suma dos números en diferentes bases y retorna el resultado en base_salida.

    Args:
        numero_a: Primer número como int
        numero_b: Segundo número como int

    Returns:
        El resultado de la suma como int en base_salida 10
    """
    # Realizar la suma en decimal
    suma = numero_a + numero_b
    return suma
